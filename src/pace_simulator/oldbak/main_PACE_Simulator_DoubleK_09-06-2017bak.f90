USE RTUTILITY, ONLY : NUMMIEANGMAX,PI
USE PACE_INSTRUMENT_DESIGN,ONLY : NWV_BAND,WV_BAND
USE MIE_PHMX_PACE, ONLY : NUM_SCAT_ANG,CEXT_ARSL_BAND,CSCAT_ARSL_BAND,&
