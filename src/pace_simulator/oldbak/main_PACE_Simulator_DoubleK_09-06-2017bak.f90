IF(WNDSPD_VAL<10.0D0)THEN
  WRITE(CFILETMP,'(f3.1)')WNDSPD_VAL
ELSE
  WRITE(CFILETMP,'(f4.1)')WNDSPD_VAL
ENDIF
CFILE1=TRIM(CFILE1)//TRIM(CFILETMP)//'TS'
