  IF(OCEAN_FCHLA_FLAG .OR. OCEAN_FCDOM_FLAG) THEN
         ALLOCATE(DIIRAD_SCALAR_AGD(NFLRSCGRID,NWATER_DEPTH),         &
                  ABSORB_COEFF_CDOM_EX(NFLRSCGRID,NWATER_DEPTH),      &
