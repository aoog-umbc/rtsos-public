    WRITE(*,*)IHEIGHT,AEXPFACMOL(IHEIGHT),LOG(MOLND_PROFILE(IHEIGHT)/MOLND_PROFILE(IHEIGHT+1))
    STOP 'CHECK AEXPFACMOL'
  ENDIF
