DO INTTMP=1,NTLYERA
  TAU_TG(INTTMP)=POLINT_SIMPLE(NPGRID,Z_FIELD,TAU_ABS_CUMSUM,ALT_LYRA(INTTMP+1),3)
ENDDO

DO INTTMP=NTLYERA,2,-1
  TAU_TG(INTTMP)=TAU_TG(INTTMP)-TAU_TG(INTTMP-1)
ENDDO

