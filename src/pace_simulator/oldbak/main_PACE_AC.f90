 ALTMID=(ALT_LYRA(ILYERA)+ALT_LYRA(ILYERA+1))/2.0
 IF(ALTMID>ALT_MOL_PROFILE(1)) THEN
   MOLND_tmp(ILYERA)=BEXPFACMOL(1)*EXP(-AEXPFACMOL(1)*(ALTMID-ALT_MOL_PROFILE(2)))
