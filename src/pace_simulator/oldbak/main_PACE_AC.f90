    ELSEIF(IAEROSOL==5)THEN! URBAN A2
       MRR1(:)=MR_URBANA2F(IWV,IRH)
       MRI1(:)=MI_URBANA2F(IWV,IRH)
       MRR2(:)=MR_URBANA2C(IWV,IRH)
