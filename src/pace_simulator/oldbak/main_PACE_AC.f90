USE RTUTILITY, ONLY : PI,FACTOR,NUMMIEANGMAX
IMPLICIT NONE
INTEGER, INTENT(IN) :: IWVEX,NQUADOINPUT
REAL*8,INTENT(IN) :: WAVELENGTH_MICRON,MU_IN
INTEGER :: ITAU,IQUAD,MVAR,I,ICOM,ILAYERW
REAL*8 :: ESUN_EX

INTEGER :: IULO,MPL,KLO
REAL*8 :: RTMP,RTMP1,RTMP2
