USE RTUTILITY,ONLY : NUMMIEANGMAX,PI

IMPLICIT REAL*8 (A-H,O-Z)                           
INTEGER ::  NANGMIE_LOCAL
REAL*8,DIMENSION(NUMMIEANGMAX,0:6)::PHMXMIE_LOCAL

