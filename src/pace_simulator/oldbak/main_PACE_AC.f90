       MRI1(:)=MI_URBANA3F(IWV+1,IRH)
       MRR2(:)=MR_URBANA3C(IWV+1,IRH)
       MRI2(:)=MI_URBANA3C(IWV+1,IRH)
    ELSEIF(IAEROSOL>=7 .AND. IAEROSOL<=9)THEN! MARINETIME
       MRR1(:)=MR_TRPOSPHERE(IWV+1,IRH)
       MRI1(:)=MI_TRPOSPHERE(IWV+1,IRH)
