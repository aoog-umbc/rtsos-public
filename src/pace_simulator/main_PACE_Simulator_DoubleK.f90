INTEGER :: INTTMP,ILAYER,IWV_INDX
INTEGER,DIMENSION(1) ::LOCINDXTMP
REAL*8 :: RTMP

IF(.NOT. GAS_ABS_FLAG)THEN
  TAU_TG=0.0D0
  TAU_TG_H2O=0.0D0
  TAU_TG_CO2=0.0D0
  TAU_TG_CH4=0.0D0
  TAU_TG_O2=0.0D0
  TAU_TG_O3=0.0D0
  TAU_TG_NO2=0.0D0
