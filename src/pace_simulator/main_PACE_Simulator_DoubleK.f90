        RNDNMBR=0.5d0
        RTMP=(0.6D0-0.06D0)*RNDNMBR(1)+0.06D0
        CPTC550=RTMP*CHLaVal**0.57D0  ! EQ. 11 in lee_data.pdf
        RTMP1=-0.4D0+(1.6D0+1.2D0*RNDNMBR(2))/(1.0D0+SQRT(CHLaVal))
