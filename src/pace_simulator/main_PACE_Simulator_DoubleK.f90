  ELCOEFF(2)=ELCOEFF(2)/TAUDIFF
  ELCOEFF(3)=DIIRAD_SCALAR_TGD(KLO)

  RTMP1=EXP(-ELCOEFF(1)*TAUDIFF1)*(ELCOEFF(3)+ELCOEFF(2)*TAUDIFF1)
  IF(ABSORPTION_TAU_REDUCE_FLAG)RTMP1=RTMP1*EXP(-TAU_REDUCE_OCEAN_PRESERVE(ILAYERW))

