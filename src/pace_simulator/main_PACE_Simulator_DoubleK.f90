ELSEIF(OCEAN_CASE_SELECT==2)THEN
!K. J. Voss, “A spectral model of the beam attenuation coefficient
!       in the ocean and coastal areas,” Limnol. Oceanogr. 37, 501-509 (1992).
    CPTC550=0.3D0*CHLaVal**0.62D0
